// ─── Territory helpers ────────────────────────────────────────────────────────

export interface TerritoryStatus {
  isAccessible: boolean;
  isActive: boolean;
  isControlled: boolean;
  isDominating: boolean;
  tierLabel: string;
  tierColor: string;
}

export const getTerritoryStatus = (
  district: District,
  playerDistrict: PlayerDistrict | undefined,
  playerLevel: number
): TerritoryStatus => {
  const isAccessible = playerLevel >= district.unlock_requirement;
  const isActive = !!playerDistrict && playerDistrict.is_unlocked;
  const territoryLevel = playerDistrict?.territory_level ?? 0;
  const marketShare = playerDistrict?.market_share ?? 0;
  const isControlled = isActive && territoryLevel >= 3;
  const isDominating = isActive && marketShare >= 0.5;

  const tierMap: Record<number, { label: string; color: string }> = {
    0: { label: 'Unexplored', color: 'text-slate-500' },
    1: { label: 'Scouting',   color: 'text-blue-400' },
    2: { label: 'Established',color: 'text-cyan-400' },
    3: { label: 'Dominant',   color: 'text-amber-400' },
    4: { label: 'Authority',  color: 'text-orange-400' },
    5: { label: 'Empire',     color: 'text-red-400' },
  };

  const tier = tierMap[Math.min(territoryLevel, 5)];

  return {
    isAccessible,
    isActive,
    isControlled,
    isDominating,
    tierLabel: isActive ? tier.label : isAccessible ? 'Available' : 'Locked',
    tierColor: isActive ? tier.color : isAccessible ? 'text-slate-300' : 'text-slate-600',
  };
};

export const xpForTerritoryLevel = (level: number): number => {
  const thresholds = [0, 200, 500, 1000, 2000, 4000];
  return thresholds[Math.min(level, 5)];
};

// XP required to reach a given player level
export const xpRequiredForLevel = (level: number): number => {
  if (level <= 1) return 0;
  return Math.floor(1000 * Math.pow(1.15, level - 2));
};

