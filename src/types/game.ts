export const getRepRank = (score: number): RepRank =>
  [...REP_RANKS].reverse().find(r => score >= r.minScore) ?? REP_RANKS[0];

export const getNextRepRank = (score: number): RepRank | null => {
  const idx = REP_RANKS.findIndex(r => score < r.minScore);
  return idx === -1 ? null : REP_RANKS[idx];
};

