export type StoryChoiceEffect = {
  money?: number;               // positive = gain, negative = cost
  xp?: number;
  reputation?: number;          // player.reputation stat
  market_share_delta?: number;  // applied to a district (0.0–1.0 range)
  rep_delta?: Partial<Record<RepTrack, number>>;
  district_id?: string | null;  // which district is affected
  // narrative tag for future unlock logic
  tag?: string;
};

export interface StoryChoice {
  id: string;         // e.g. 'accept' | 'decline' | 'negotiate'
  label: string;      // short CTA label
  description: string;// one-line consequence hint
  effects: StoryChoiceEffect;
  // Optional min requirements to show this choice
  requiresLevel?: number;
  requiresRepTrack?: { track: RepTrack; minScore: number };
}

export type StoryCategory =
  | 'investor'      // capital / fund events
  | 'owner'         // hotel owner events
  | 'market'        // macro / sector events
  | 'competitor'    // rival advisory firms
  | 'opportunity';  // off-market / rare finds

export interface StoryEventTemplate {
  id: string;
  category: StoryCategory;
  title: string;
  body: string;                 // 2-3 sentence narrative
  accentColor: string;
  icon: string;                 // lucide icon name (string for lookup)
  choices: StoryChoice[];
  // Spawn conditions (all optional — missing = always eligible)
  minLevel?: number;
  requiresActiveDistrict?: boolean;
  cooldownHours?: number;       // min hours between same template firing
}

